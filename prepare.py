def extract_strings_to_file(in_file: str, out_file: str):
    """
    Extracts strings from game files and saves them to output files.
    
    This function handles two main cases:
    1. For Japanese text files (those without non-Japanese language formats),
       extracts strings to the jp/ directory
    2. For other files, extracts strings to the source/ directory
    
    Args:
        - in_file (str): Path to input file relative to unpacked/ directory
        - out_file (str): Output filename to save extracted strings
    
    The function uses getstr.extract_strings_to_file() for extraction except for
    .txt files which are simply copied to the destination.
    """
    console.print(f"Extracting strings from {in_file}")

    nonJapaneseFormats = {k: v for k, v in langFormats.items() if k != 'jp'}
    if not any(fmt in in_file for lang in nonJapaneseFormats.values() for fmt in lang.values()):
        if in_file.endswith(".txt"):
            shutil.copy(f"unpacked/{in_file}", f"jp/{out_file}")
            return
        getstr.extract_strings_to_file(f"unpacked/{in_file}", f"jp/{out_file}", lang="jp", keep_non_ascii=True)
        return

    if in_file.endswith(".txt"):
        shutil.copy(f"unpacked/{in_file}", f"source/{out_file}")
    else:
        getstr.extract_strings_to_file(f"unpacked/{in_file}", f"source/{out_file}", keep_non_ascii=True)

